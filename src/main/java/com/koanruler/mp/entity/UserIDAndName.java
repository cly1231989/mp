package com.koanruler.mp.entity;

public class UserIDAndName {
	private int id;
	private int type;
	private String name;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public UserIDAndName(int id, int type, String name) {
		super();
		this.id = id;
		this.type = type;
		this.name = name;
	}
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
}
